def white(t):
    return "\033[97m" + t + "\033[0m"

def resource_path(relative_path):
    """ Get absolute path to resource, works for dev and for PyInstaller """
    try:
        # PyInstaller creates a temp folder and stores path in _MEIPASS
        base_path = sys._MEIPASS
    except Exception:
        base_path = os.path.abspath(".")

    return os.path.join(base_path, relative_path)

def score_file_path():
    appdata = os.getenv('APPDATA')
    game_folder = os.path.join(appdata, 'PokemonGuessGame')
    os.makedirs(game_folder, exist_ok=True)
    return os.path.join(game_folder, 'score.txt')

def wait_before_exit():
    input(blue("\nPress ENTER to exit..."))

def load(l):
    file_path = resource_path(f"data/{l}.txt")
    f = open(file_path, "r", encoding="utf-8")
    pok= f.read().splitlines()
    f.close()
    return pok

def load_asci(n):
    file_path = resource_path(f"data/{n}.txt")
    f = open(file_path, "r", encoding="utf-8")
    art = f.read()
    f.close()
    return art
def load_scores():
    defaults = {"easy": 0, "medium": 0, "hard": 0}
    score_path = score_file_path()
    if not os.path.exists(score_path):
        return defaults
    try:
        with open(score_path, "r") as f:
            data = {}
            for line in f:
                if ":" in line:
                    mode, score = line.strip().split(":", 1)
                    data[mode] = int(score)
    except Exception:
        return defaults
    for k in defaults:
        if k not in data:
            data[k] = 0
    return data

def save_scores(scores):
    score_path = score_file_path()
    with open(score_path, "w") as f:
        for i, j in scores.items():
            f.write(f"{i}:{j}\n")

